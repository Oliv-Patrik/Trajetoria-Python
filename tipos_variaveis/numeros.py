# Inteiro (Int)
numero_inteiro = 5
print("Inteiro = ", numero_inteiro)

# Real com número flutuante (Float)
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# Verificar o tipo da VARIAVEL (Type())
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

#Soma = +
soma = 1 + 1
print("1 + 1 = ", soma)

#Subtração = -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

#Divisão = /
divisao = 4 / 2
print("4 / 2 = ", divisao)

#Multiplicação = *
multiplicacao = 2 * 4
print("2 x 4 = ", multiplicacao)